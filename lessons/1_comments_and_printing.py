# Comments and Printing

# A line that starts with a '#' character will be read as a comment in Python

# This means it will be ignored, and the lines of code before and after it will run, as if this
# does not exist

# If you have some code and then a '#' afterwards, everything from the '#' onwards will be ignored
# but the first part of the line will be interpreted as normal code

# Here is an example of a print statement in Python
print("Hello world!")

